extern FLO_type __pack_d ( fp_number_type * );
FLO_type
__pack_d ( fp_number_type * src)
{
  FLO_union_type dst;
  fractype fraction = src->fraction.ll;
  int sign = src->sign;
  int exp = 0;
  if (0 && (isnan (src) || isinf (src)))
    {
      exp = (0x7ff);
      fraction = ((fractype) 1 << 52) - 1;
    }
  else if (isnan (src))
    {
      exp = (0x7ff);
      if (src->class == CLASS_QNAN || 1)
 {
   fraction |= 0x8000000000000LL - 1;
 }
    }
  else if (isinf (src))
    {
      exp = (0x7ff);
      fraction = 0;
    }
  else if (iszero (src))
    {
      exp = 0;
      fraction = 0;
    }
  else if (fraction == 0)
    {
      exp = 0;
    }
  else
    {
      if (__builtin_expect (src->normal_exp < (-(1023)+1), 0))
 {
   int shift = (-(1023)+1) - src->normal_exp;
   exp = 0;
   if (shift > 64 - 8L)
     {
       fraction = 0;
     }
   else
     {
       int lowbit = (fraction & (((fractype)1 << shift) - 1)) ? 1 : 0;
       fraction = (fraction >> shift) | lowbit;
     }
   if ((fraction & 0xff) == 0x80)
     {
       if ((fraction & (1 << 8L)))
  fraction += 0x7f + 1;
     }
   else
     {
       fraction += 0x7f;
     }
   if (fraction >= ((fractype)1<<(52 +8L)))
     {
       exp += 1;
     }
   fraction >>= 8L;
 }
      else if (!0
        && __builtin_expect (src->normal_exp > 1023, 0))
 {
   exp = (0x7ff);
   fraction = 0;
 }
      else
 {
   exp = src->normal_exp + 1023;
   if (!0)
     {
       if ((fraction & 0xff) == 0x80)
  {
    if (fraction & (1 << 8L))
      fraction += 0x7f + 1;
  }
       else
  {
    fraction += 0x7f;
  }
       if (fraction >= ((fractype)1<<(52 +1+8L)))
  {
    fraction >>= 1;
    exp += 1;
  }
     }
   fraction >>= 8L;
   if (0 && exp > (0x7ff))
     {
       exp = (0x7ff);
       fraction = ((fractype) 1 << 52) - 1;
     }
 }
    }
  dst.value_raw = fraction & ((((fractype)1) << 52) - (fractype)1);
  dst.value_raw |= ((fractype) (exp & ((1 << 11) - 1))) << 52;
  dst.value_raw |= ((fractype) (sign & 1)) << (52 | 11);
  return dst.value;
}
